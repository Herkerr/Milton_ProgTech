public void PrintCustomerDetails(Customer customer)
{
    Console.WriteLine("Customer Name: " + customer.Name);
    Console.WriteLine("Customer Age: " + customer.Age);
    Console.WriteLine("Customer Address: " + customer.Address);
    Console.WriteLine("Customer Email: " + customer.Email);
    Console.WriteLine("Customer Phone: " + customer.Phone);
    Console.WriteLine("Customer Account Balance: " + customer.AccountBalance);
}
